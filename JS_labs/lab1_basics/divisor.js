/**
 * Находит наибольший делитель числа из списка [10, 7, 6, 3, 2].
 * @param {number} number - Число для проверки.
 * @returns {string} - Сообщение о наибольшем делителе или "Не делится".
 */
function findLargestDivisor(number) {
  // Проверяем делимость в порядке убывания делителей, чтобы найти наибольший [5]
  if (number % 10 === 0) {
    return `Число делится на 10`;
  } else if (number % 7 === 0) {
    return `Число делится на 7`;
  } else if (number % 6 === 0) {
    // Проверка на 6 идет после 10 и 7, но до 3 и 2,
    // так как 6 > 3 и 6 > 2. Если делится на 6, то и на 2, и на 3. [5]
    return `Число делится на 6`;
  } else if (number % 3 === 0) {
    return `Число делится на 3`;
  } else if (number % 2 === 0) {
    // Эта проверка сработает, только если число не делится на 10 и 6.
    return `Число делится на 2`;
  } else {
    // Если ни один делитель не подошел
    return "Не делится";
  }
}

// Примеры использования из задания [5]:
console.log(`Проверка числа 30: ${findLargestDivisor(30)}`);   // Ожидаемый вывод: Число делится на 10
console.log(`Проверка числа 15: ${findLargestDivisor(15)}`);   // Ожидаемый вывод: Число делится на 3
console.log(`Проверка числа 12: ${findLargestDivisor(12)}`);   // Ожидаемый вывод: Число делится на 6
console.log(`Проверка числа 1643: ${findLargestDivisor(1643)}`); // Ожидаемый вывод: Не делится
console.log(`Проверка числа 14: ${findLargestDivisor(14)}`);   // Ожидаемый вывод: Число делится на 7
console.log(`Проверка числа 8: ${findLargestDivisor(8)}`);    // Ожидаемый вывод: Число делится на 2

