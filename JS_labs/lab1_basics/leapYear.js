/**
 * Проверяет, является ли год високосным.
 * @param {number} year - Год для проверки.
 * @returns {string} - "yes", если год високосный, иначе "no".
 */
function isLeapYear(year) {
  // Условие високосного года [5]:
  // (делится на 4 И НЕ делится на 100) ИЛИ (делится на 400)
  const leap = (year % 4 === 0 && year % 100 !== 0) || (year % 400 === 0);

  // Возвращаем "yes" или "no" в зависимости от результата
  return leap ? "yes" : "no";
}

// Примеры использования из задания [5]:
console.log(`Год 1984: ${isLeapYear(1984)}`); // Ожидаемый вывод: yes
console.log(`Год 2003: ${isLeapYear(2003)}`); // Ожидаемый вывод: no
console.log(`Год 4: ${isLeapYear(4)}`);       // Ожидаемый вывод: yes
console.log(`Год 1900: ${isLeapYear(1900)}`); // Ожидаемый вывод: no (делится на 100, но не на 400)
console.log(`Год 2000: ${isLeapYear(2000)}`); // Ожидаемый вывод: yes (делится на 400)

