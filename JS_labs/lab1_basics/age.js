/**
 * Определяет возрастную категорию по числу лет.
 * @param {number} age - Возраст человека.
 * @returns {string} - Возрастная категория ('младенец', 'ребенок', 'подросток', 'взрослый', 'пожилой') или сообщение об ошибке.
 */
function determineAgeCategory(age) {
  // Проверяем корректность введенного возраста
  if (age < 0) {
    return "Некорректный возраст"; // Возраст не может быть отрицательным
  }

  // Определяем категорию на основе возраста
  if (age >= 0 && age <= 2) {
    return "младенец";
  } else if (age >= 3 && age <= 13) {
    return "ребенок";
  } else if (age >= 14 && age <= 19) {
    return "подросток";
  } else if (age >= 20 && age <= 65) {
    return "взрослый";
  } else { // age >= 66
    return "пожилой";
  }
}

// Примеры использования из задания [5]:
console.log(`Возраст 20: ${determineAgeCategory(20)}`);  // Ожидаемый вывод: взрослый
console.log(`Возраст 1: ${determineAgeCategory(1)}`);    // Ожидаемый вывод: младенец
console.log(`Возраст 100: ${determineAgeCategory(100)}`); // Ожидаемый вывод: пожилой
console.log(`Возраст -5: ${determineAgeCategory(-5)}`); // Ожидаемый вывод: Некорректный возраст

